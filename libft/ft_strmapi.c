#include "libft.h"

char	*ft_strmapi(char const *s, char (*f)(unsigned int, char))
{
	char	*result;
	int		strlen;
	int		i;

	i = 0;
	strlen = ft_strlen(s);
	result = (char *)malloc(sizeof(char) * (strlen + 1));
	if (result == 0)
		return (0);
	while (s[i])
	{
		result[i] = f(i, s[i]);
		i++;
	}
	result[i] = 0;
	return (result);
}
